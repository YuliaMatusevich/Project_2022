public class OddEven {
    //В классе OddEven создать алгоритм oddEven(),
    // который принимает на вход целое число, возвращает “Odd”,  если число нечетное,
    // и “Even”, если число четное.
    //

    public String oddEven (int x){
        if(x % 2 == 0){
            return "Even";
        } return "Odd";
    }
}
